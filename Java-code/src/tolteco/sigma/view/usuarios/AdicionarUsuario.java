    private static final String EMPTY="";
    
    @Override
    public void cleanAllFields() {
        UserNameField.setText(EMPTY);
        PassField.setText(EMPTY);
        FuncButton.setSelected(true);
        AdminButton.setSelected(false);
        RootButton.setSelected(false);
    }

    @Override
    public void fillAllFields(Usuario object) {
        UserNameField.setText(object.getUserName());
        PassField.setText(Arrays.toString(object.getPass()));
        
        switch(object.getAccessLevel()){
            case FUNCIONARIO:
                FuncButton.setSelected(true); break;
            case ADMINISTRADOR:
                AdminButton.setSelected(true); break;
            case ROOT:
                RootButton.setSelected(true); break;
        }
    }

    @Override
    public Usuario getInstance() {
        String nome = UserNameField.getText();
        char[] pass = PassField.getText().toCharArray();
        Access level = null;
        
        if (FuncButton.isSelected()){
            level = Access.FUNCIONARIO;
        } else if (AdminButton.isSelected()){
            level = Access.ADMINISTRADOR;
        } else if (RootButton.isSelected()){
            level = Access.ROOT;
        }
        
        return new Usuario(-1, nome, level, pass);
    }
